---@param r number
---@param g number
---@param b number
---@param a number
---@param scale number
---@return HDRColor
function color.new(r, g, b, a, scale)-- Returns a HDRColor, use 255 as scale to make the values scale from 0-255 instead of 0-1
    local alpha = a or 1
    local s = scale or 1

    local red = r / s
    local green = g / s
    local blue = b / s

    return HDRColor.new({ Red = red, Green = green, Blue = blue, Alpha = alpha })
end

